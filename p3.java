public class Main {
    public static void main(String[] args) {
        // Perulangan untuk mengecek setiap bilangan dari 1-100
        for (int i = 1; i <= 100; i++) {
            // Cek apakah bilangan tersebut ganjil
            if (i % 2 != 0) {
                System.out.println(i);
            }
        }
    }
}
